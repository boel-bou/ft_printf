#include "ft_printf.h"

void	ft_putuns_pr(unsigned int n, size_t *cmp)
{
	if (n >= 10)
		ft_putuns_pr(n / 10, cmp);
	ft_putchar_pr(n % 10 + '0', cmp);
}
